fn expand_expr(
    expr: ShapeExpr,
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    hint: &str,
    acc: &mut Vec<TokenStream>,
) -> Result<TokenStream, TokenStream> {
    match expr {
        ShapeExpr::Rebuild { source } => {
            let (src_ident, src_ty_args) = expand_node(source, hint, acc)?;
            rebuild(attrs, target, target_generics, allow_inherit, src_ident, src_ty_args)
        }
        ShapeExpr::Omit { source, fields } => {
            let (src_ident, src_ty_args) = expand_node(source, hint, acc)?;
            omit(attrs, target, target_generics, allow_inherit, src_ident, src_ty_args, fields)
        }
        ShapeExpr::Pick { source, fields } => {
            let (src_ident, src_ty_args) = expand_node(source, hint, acc)?;
            pick(attrs, target, target_generics, allow_inherit, src_ident, src_ty_args, fields)
        }
        ShapeExpr::Merge { left, right } => {
            let (left_ident, left_ty_args) = expand_node(left, hint, acc)?;
            let (right_ident, right_ty_args) = expand_node(right, hint, acc)?;
            merge(attrs, target, target_generics, allow_inherit, left_ident, left_ty_args, right_ident, right_ty_args)
        }
        ShapeExpr::Partial { source } => {
            let (src_ident, src_ty_args) = expand_node(source, hint, acc)?;
            partial(attrs, target, target_generics, allow_inherit, src_ident, src_ty_args)
        }
        ShapeExpr::Required { source } => {
            let (src_ident, src_ty_args) = expand_node(source, hint, acc)?;
            required(attrs, target, target_generics, allow_inherit, src_ident, src_ty_args)
        }
        ShapeExpr::Diff { left, right } => {
            let (left_ident, left_ty_args) = expand_node(left, hint, acc)?;
            let (right_ident, _) = expand_node(right, hint, acc)?;
            diff(attrs, target, target_generics, allow_inherit, left_ident, left_ty_args, right_ident)
        }
    }
}

/// Resolve a `ShapeNode` to `(concrete_ident, optional_explicit_ty_args)`.
///
/// - Leaf returns its ident and explicit ty_args directly.
/// - Composed generates an anonymous type (always with auto-inherited generics)
///   and returns its ident with no explicit ty_args.
fn expand_node(
    node: ShapeNode,
    hint: &str,
    acc: &mut Vec<TokenStream>,
) -> Result<(Ident, Option<TokenStream>), TokenStream> {
    match node {
        ShapeNode::Leaf(ident, ty_args) => Ok((ident, ty_args)),
        ShapeNode::Composed(expr) => {
            let id = next_anon_id();
            let anon_name = format!("__TypeshaperAnon_{}_{}", hint, id);
            let anon_ident: Ident = syn::parse_str(&anon_name).map_err(|e| {
                syn::Error::new(
                    proc_macro2::Span::call_site(),
                    format!(
                        "typeshaper: internal error generating anonymous type `{}`: {}",
                        anon_name, e
                    ),
                )
                .to_compile_error()
            })?;
            // Anonymous intermediates always allow generic auto-inherit (no user-specified ones).
            let ts = expand_expr(
                *expr,
                &[],
                anon_ident.clone(),
                &syn::Generics::default(),
                true,
                hint,
                acc,
            )?;
            acc.push(ts);
            Ok((anon_ident, None))
        }
    }
}

// ---------------------------------------------------------------------------
// T  →  Rebuild
// ---------------------------------------------------------------------------

type R = Result<TokenStream, TokenStream>;

fn rebuild(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    source: Ident,
    source_ty_args: Option<TokenStream>,
) -> R {
    let src_entry = try_lookup(&source)?;
    if !allow_inherit {
        if let Some(err) = check_no_implicit_generics(&source, &src_entry, target_generics) {
            return Err(err);
        }
    }
    let (names, types, vises) = to_token_vecs(&src_entry.fields)?;

    let eff_gen = effective_generics(target_generics, &src_entry, allow_inherit);
    let (impl_g, ty_g, where_c) = split(&eff_gen);
    let src_type = source_type(&source, source_ty_args.as_ref(), &ty_g);

    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(src_entry.fields, gen_str, where_str),
    );

    Ok(quote! {
        #(#attrs)*
        pub struct #target #impl_g #where_c {
            #(#vises #names: #types,)*
        }

        impl #impl_g ::typeshaper::TypeshaperInto<#target #ty_g> for #src_type #where_c {
            fn typeshaper_into(self) -> #target #ty_g {
                #target { #(#names: self.#names,)* }
            }
        }
    })
}

// ---------------------------------------------------------------------------
// T - [fields]  →  Omit
// ---------------------------------------------------------------------------

fn omit(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    source: Ident,
    source_ty_args: Option<TokenStream>,
    omit_fields: Vec<Ident>,
) -> R {
    let src_entry = try_lookup(&source)?;
    if !allow_inherit {
        if let Some(err) = check_no_implicit_generics(&source, &src_entry, target_generics) {
            return Err(err);
        }
    }

    // H-3: reject empty omit list
    if omit_fields.is_empty() {
        return Err(syn::Error::new(
            proc_macro2::Span::call_site(),
            "omit field list must not be empty; use `T` (Rebuild) to copy all fields",
        )
        .to_compile_error());
    }

    // B-2: check for duplicate fields and non-existent fields
    let mut seen = std::collections::HashSet::new();
    for f in &omit_fields {
        let name = f.to_string();
        if !seen.insert(name.clone()) {
            return Err(syn::Error::new(
                f.span(),
                format!("field `{}` is listed more than once in the omit list", name),
            )
            .to_compile_error());
        }
        if !src_entry.fields.iter().any(|d| d.name == name) {
            return Err(field_not_found(f, &source));
        }
    }
    let omit_set = seen;

    let kept: Vec<FieldDef> = src_entry
        .fields
        .into_iter()
        .filter(|f| !omit_set.contains(&f.name))
        .collect();
    let (names, types, vises) = to_token_vecs(&kept)?;

    let eff_gen = {
        let src_for_inherit = TypeEntry::with_generics(
            kept.clone(),
            src_entry.generics_tokens.clone(),
            src_entry.where_tokens.clone(),
        );
        effective_generics(target_generics, &src_for_inherit, allow_inherit)
    };
    let (impl_g, ty_g, where_c) = split(&eff_gen);
    let src_type = source_type(&source, source_ty_args.as_ref(), &ty_g);

    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(kept, gen_str, where_str),
    );

    Ok(quote! {
        #(#attrs)*
        pub struct #target #impl_g #where_c {
            #(#vises #names: #types,)*
        }

        impl #impl_g ::typeshaper::TypeshaperInto<#target #ty_g> for #src_type #where_c {
            fn typeshaper_into(self) -> #target #ty_g {
                #target { #(#names: self.#names,)* }
            }
        }
    })
}

// ---------------------------------------------------------------------------
// T & [fields]  →  Pick
// ---------------------------------------------------------------------------

fn pick(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    source: Ident,
    source_ty_args: Option<TokenStream>,
    pick_fields: Vec<Ident>,
) -> R {
    let src_entry = try_lookup(&source)?;
    if !allow_inherit {
        if let Some(err) = check_no_implicit_generics(&source, &src_entry, target_generics) {
            return Err(err);
        }
    }

    // H-3: reject empty pick list
    if pick_fields.is_empty() {
        return Err(syn::Error::new(
            proc_macro2::Span::call_site(),
            "pick field list must not be empty; use `T` (Rebuild) to copy all fields",
        )
        .to_compile_error());
    }

    let mut seen = std::collections::HashSet::new();
    let mut kept: Vec<FieldDef> = Vec::new();
    for f in &pick_fields {
        let name = f.to_string();
        if !seen.insert(name.clone()) {
            return Err(syn::Error::new(
                f.span(),
                format!("field `{}` is listed more than once in the pick list", name),
            )
            .to_compile_error());
        }
        match src_entry.fields.iter().find(|d| d.name == name) {
            Some(d) => kept.push(d.clone()),
            None => return Err(field_not_found(f, &source)),
        }
    }

    let (names, types, vises) = to_token_vecs(&kept)?;
    let eff_gen = {
        let src_for_inherit = TypeEntry::with_generics(
            kept.clone(),
            src_entry.generics_tokens.clone(),
            src_entry.where_tokens.clone(),
        );
        effective_generics(target_generics, &src_for_inherit, allow_inherit)
    };
    let (impl_g, ty_g, where_c) = split(&eff_gen);
    let src_type = source_type(&source, source_ty_args.as_ref(), &ty_g);

    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(kept, gen_str, where_str),
    );

    Ok(quote! {
        #(#attrs)*
        pub struct #target #impl_g #where_c {
            #(#vises #names: #types,)*
        }

        impl #impl_g ::typeshaper::TypeshaperInto<#target #ty_g> for #src_type #where_c {
            fn typeshaper_into(self) -> #target #ty_g {
                #target { #(#names: self.#names,)* }
            }
        }
    })
}

// ---------------------------------------------------------------------------
// A + B  →  Merge
// ---------------------------------------------------------------------------

fn merge(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    left: Ident,
    left_ty_args: Option<TokenStream>,
    right: Ident,
    right_ty_args: Option<TokenStream>,
) -> R {
    let entry_a = try_lookup(&left)?;
    let entry_b = try_lookup(&right)?;
    if !allow_inherit {
        // For merge, error if EITHER source is generic and target has no explicit params.
        if (entry_a.is_generic() || entry_b.is_generic())
            && target_generics.params.is_empty()
            && target_generics.where_clause.is_none()
        {
            let offender = if entry_a.is_generic() { &left } else { &right };
            let offender_entry = if entry_a.is_generic() { &entry_a } else { &entry_b };
            if let Some(err) =
                check_no_implicit_generics(offender, offender_entry, target_generics)
            {
                return Err(err);
            }
        }
    }

    let names_a_set: std::collections::HashSet<&str> =
        entry_a.fields.iter().map(|f| f.name.as_str()).collect();
    for f in &entry_b.fields {
        if names_a_set.contains(f.name.as_str()) {
            return Err(syn::Error::new(
                right.span(),
                format!(
                    "field `{}` exists in both `{}` and `{}`",
                    f.name, left, right
                ),
            )
            .to_compile_error());
        }
    }

    let (names_a, types_a, vises_a) = to_token_vecs(&entry_a.fields)?;
    let (names_b, types_b, vises_b) = to_token_vecs(&entry_b.fields)?;

    // Target generics: use explicit if provided; otherwise merge both sources (allow_inherit only).
    let eff_gen = if !target_generics.params.is_empty() || target_generics.where_clause.is_some() {
        target_generics.clone()
    } else if allow_inherit {
        // Auto-merge both source generics (only for anonymous intermediates).
        // Uses syn-based deduplication to avoid <T, T> collisions (B-3).
        merge_generics(&entry_a, &entry_b)
    } else {
        syn::Generics::default()
    };
    let (impl_g, ty_g, where_c) = split(&eff_gen);

    // Source types in impl: use explicit ty_args or fall back to respective
    // registered ty_generics.
    let left_ty = match left_ty_args.as_ref() {
        Some(a) => quote!(#left #a),
        None => {
            let g = parse_stored_generics(&entry_a);
            let (_, tg, _) = g.split_for_impl();
            quote!(#left #tg)
        }
    };
    let right_ty = match right_ty_args.as_ref() {
        Some(a) => quote!(#right #a),
        None => {
            let g = parse_stored_generics(&entry_b);
            let (_, tg, _) = g.split_for_impl();
            quote!(#right #tg)
        }
    };

    let mut all_fields = entry_a.fields;
    all_fields.extend(entry_b.fields);
    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(all_fields, gen_str, where_str),
    );

    Ok(quote! {
        #(#attrs)*
        pub struct #target #impl_g #where_c {
            #(#vises_a #names_a: #types_a,)*
            #(#vises_b #names_b: #types_b,)*
        }

        impl #impl_g From<(#left_ty, #right_ty)> for #target #ty_g #where_c {
            fn from((a, b): (#left_ty, #right_ty)) -> Self {
                #target {
                    #(#names_a: a.#names_a,)*
                    #(#names_b: b.#names_b,)*
                }
            }
        }
    })
}

// ---------------------------------------------------------------------------
// T?  →  Partial
// ---------------------------------------------------------------------------

fn partial(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    source: Ident,
    source_ty_args: Option<TokenStream>,
) -> R {
    let src_entry = try_lookup(&source)?;
    if !allow_inherit {
        if let Some(err) = check_no_implicit_generics(&source, &src_entry, target_generics) {
            return Err(err);
        }
    }

    let mut opt_fields: Vec<FieldDef> = Vec::with_capacity(src_entry.fields.len());
    let mut already_opt: Vec<bool> = Vec::with_capacity(src_entry.fields.len());

    for f in &src_entry.fields {
        if f.unwrapped_ty.is_some() {
            opt_fields.push(f.clone());
            already_opt.push(true);
        } else if let Some(inner) = try_unwrap_option(&f.ty_tokens) {
            opt_fields.push(FieldDef {
                name: f.name.clone(),
                vis: f.vis.clone(),
                ty_tokens: f.ty_tokens.clone(),
                unwrapped_ty: Some(inner),
            });
            already_opt.push(true);
        } else {
            opt_fields.push(FieldDef::wrapped_optional(
                f.name.clone(),
                f.vis.clone(),
                f.ty_tokens.clone(),
            ));
            already_opt.push(false);
        }
    }

    let (names, opt_types, vises) = to_token_vecs(&opt_fields)?;
    let eff_gen = {
        let src_for_inherit = TypeEntry::with_generics(
            opt_fields.clone(),
            src_entry.generics_tokens.clone(),
            src_entry.where_tokens.clone(),
        );
        effective_generics(target_generics, &src_for_inherit, allow_inherit)
    };
    let (impl_g, ty_g, where_c) = split(&eff_gen);
    let src_type = source_type(&source, source_ty_args.as_ref(), &ty_g);

    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(opt_fields, gen_str, where_str),
    );

    let from_exprs: Vec<TokenStream> = names
        .iter()
        .zip(already_opt.iter())
        .map(|(name, &already)| {
            if already {
                quote! { #name: src.#name }
            } else {
                quote! { #name: Some(src.#name) }
            }
        })
        .collect();

    Ok(quote! {
        #(#attrs)*
        pub struct #target #impl_g #where_c {
            #(#vises #names: #opt_types,)*
        }

        impl #impl_g From<#src_type> for #target #ty_g #where_c {
            fn from(src: #src_type) -> Self {
                #target { #(#from_exprs,)* }
            }
        }
    })
}

// ---------------------------------------------------------------------------
// T!  →  Required
// ---------------------------------------------------------------------------

fn try_unwrap_option(ty_tokens: &str) -> Option<String> {
    let ty: syn::Type = syn::parse_str(ty_tokens).ok()?;
    if let syn::Type::Path(tp) = ty {
        if tp.qself.is_some() {
            return None;
        }
        let segs: Vec<_> = tp.path.segments.iter().collect();
        let is_std_option = match segs.as_slice() {
            [s] if s.ident == "Option" => true,
            [a, b, c]
                if (a.ident == "std" || a.ident == "core")
                    && b.ident == "option"
                    && c.ident == "Option" =>
            {
                true
            }
            _ => false,
        };
        if !is_std_option {
            return None;
        }
        let last = segs.last()?;
        if let syn::PathArguments::AngleBracketed(ref args) = last.arguments {
            if args.args.len() == 1 {
                if let syn::GenericArgument::Type(inner) = &args.args[0] {
                    return Some(quote!(#inner).to_string());
                }
            }
        }
    }
    None
}

fn required(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    source: Ident,
    source_ty_args: Option<TokenStream>,
) -> R {
    let src_entry = try_lookup(&source)?;
    if !allow_inherit {
        if let Some(err) = check_no_implicit_generics(&source, &src_entry, target_generics) {
            return Err(err);
        }
    }

    let inner_fields: Vec<FieldDef> = src_entry
        .fields
        .iter()
        .map(|f| {
            let inner_ty = f
                .unwrapped_ty
                .clone()
                .or_else(|| try_unwrap_option(&f.ty_tokens));
            match inner_ty {
                Some(inner) => FieldDef::plain(f.name.clone(), f.vis.clone(), inner),
                None => f.clone(),
            }
        })
        .collect();

    let needs_unwrap: Vec<bool> = src_entry
        .fields
        .iter()
        .map(|f| f.unwrapped_ty.is_some() || try_unwrap_option(&f.ty_tokens).is_some())
        .collect();

    let (names, inner_types, vises) = to_token_vecs(&inner_fields)?;
    let eff_gen = {
        let src_for_inherit = TypeEntry::with_generics(
            inner_fields.clone(),
            src_entry.generics_tokens.clone(),
            src_entry.where_tokens.clone(),
        );
        effective_generics(target_generics, &src_for_inherit, allow_inherit)
    };
    let (impl_g, ty_g, where_c) = split(&eff_gen);
    let src_type = source_type(&source, source_ty_args.as_ref(), &ty_g);

    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(inner_fields, gen_str, where_str),
    );

    let any_needs_unwrap = needs_unwrap.iter().any(|&b| b);

    if any_needs_unwrap {
        let try_from_exprs: Vec<TokenStream> = names
            .iter()
            .zip(needs_unwrap.iter())
            .map(|(name, &unwrap)| {
                if unwrap {
                    quote! {
                        #name: src.#name.ok_or(
                            ::typeshaper::RequiredError::new(stringify!(#name))
                        )?
                    }
                } else {
                    quote! { #name: src.#name }
                }
            })
            .collect();

        Ok(quote! {
            #(#attrs)*
            pub struct #target #impl_g #where_c {
                #(#vises #names: #inner_types,)*
            }

            impl #impl_g TryFrom<#src_type> for #target #ty_g #where_c {
                type Error = ::typeshaper::RequiredError;

                fn try_from(src: #src_type) -> Result<Self, Self::Error> {
                    Ok(Self { #(#try_from_exprs,)* })
                }
            }
        })
    } else {
        Ok(quote! {
            #(#attrs)*
            pub struct #target #impl_g #where_c {
                #(#vises #names: #inner_types,)*
            }

            impl #impl_g From<#src_type> for #target #ty_g #where_c {
                fn from(src: #src_type) -> Self {
                    Self { #(#names: src.#names,)* }
                }
            }
        })
    }
}

// ---------------------------------------------------------------------------
// A % B  →  Diff
// ---------------------------------------------------------------------------

fn diff(
    attrs: &[Attribute],
    target: Ident,
    target_generics: &syn::Generics,
    allow_inherit: bool,
    left: Ident,
    left_ty_args: Option<TokenStream>,
    right: Ident,
) -> R {
    let entry_a = try_lookup(&left)?;
    let entry_b = try_lookup(&right)?;
    if !allow_inherit {
        if let Some(err) = check_no_implicit_generics(&left, &entry_a, target_generics) {
            return Err(err);
        }
    }

    let kept: Vec<FieldDef> = entry_a
        .fields
        .clone()
        .into_iter()
        .filter(|f| {
            !entry_b.fields.iter().any(|fb| {
                fb.name == f.name
                    && normalize_ty(&fb.ty_tokens) == normalize_ty(&f.ty_tokens)
            })
        })
        .collect();

    if kept.is_empty() {
        return Err(syn::Error::new(
            left.span(),
            format!("`{}` has no fields that are absent from `{}`", left, right),
        )
        .to_compile_error());
    }

    let (names, types, vises) = to_token_vecs(&kept)?;
    // Diff inherits left source's generics.
    let eff_gen = {
        let src_for_inherit = TypeEntry::with_generics(
            kept.clone(),
            entry_a.generics_tokens.clone(),
            entry_a.where_tokens.clone(),
        );
        effective_generics(target_generics, &src_for_inherit, allow_inherit)
    };
    let (impl_g, ty_g, where_c) = split(&eff_gen);
    let left_type = source_type(&left, left_ty_args.as_ref(), &ty_g);

    let (gen_str, where_str) = encode_generics(&eff_gen);
    register(
        crate_key(),
        target.to_string(),
        TypeEntry::with_generics(kept, gen_str, where_str),
    );

    Ok(quote! {
        #(#attrs)*
        pub struct #target #impl_g #where_c {
            #(#vises #names: #types,)*
        }

        impl #impl_g ::typeshaper::TypeshaperInto<#target #ty_g> for #left_type #where_c {
            fn typeshaper_into(self) -> #target #ty_g {
                #target { #(#names: self.#names,)* }
            }
        }
    })
}

