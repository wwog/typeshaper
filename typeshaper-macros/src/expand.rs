fn normalize_ty(ty_tokens: &str) -> String {
    syn::parse_str::<syn::Type>(ty_tokens)
        .map(|ty| quote!(#ty).to_string())
        .unwrap_or_else(|_| ty_tokens.to_owned())
}

fn try_lookup(ident: &Ident) -> Result<TypeEntry, TokenStream> {
    let file = crate_key();
    let name = ident.to_string();
    if let Some(entry) = lookup(file, &name) {
        return Ok(entry);
    }
    if let Some(entry) = lookup(None, &name) {
        return Ok(entry);
    }
    if let Some(entry) = lookup_exported(&name) {
        return Ok(entry);
    }
    Err(syn::Error::new(
        ident.span(),
        format!(
            "type `{}` is not registered — \
             apply `#[typeshaper]` to it, or create it with `typex!()` first",
            ident
        ),
    )
    .to_compile_error())
}

fn field_not_found(field: &Ident, source: &Ident) -> TokenStream {
    syn::Error::new(
        field.span(),
        format!("field `{}` does not exist on `{}`", field, source),
    )
    .to_compile_error()
}

fn extract_type_entry(input: &DeriveInput) -> Result<TypeEntry, TokenStream> {
    let fields = match &input.data {
        Data::Struct(data) => match &data.fields {
            Fields::Named(named) => named
                .named
                .iter()
                .filter_map(|f| {
                    f.ident.as_ref().map(|ident| {
                        let ty = &f.ty;
                        let vis = &f.vis;
                        FieldDef::plain(
                            ident.to_string(),
                            quote!(#vis).to_string(),
                            quote!(#ty).to_string(),
                        )
                    })
                })
                .collect(),
            _ => {
                return Err(syn::Error::new_spanned(
                    &input.ident,
                    "#[typeshaper] only supports structs with named fields",
                )
                .to_compile_error())
            }
        },
        _ => {
            return Err(syn::Error::new_spanned(
                &input.ident,
                "#[typeshaper] only supports structs",
            )
            .to_compile_error())
        }
    };

    let params = &input.generics.params;
    let generics_tokens = if params.is_empty() {
        String::new()
    } else {
        quote!(<#params>).to_string()
    };
    let where_tokens = input
        .generics
        .where_clause
        .as_ref()
        .map(|wc| quote!(#wc).to_string())
        .unwrap_or_default();

    Ok(TypeEntry::with_generics(fields, generics_tokens, where_tokens))
}

/// Returns a stable string identifier for a generic parameter, used for deduplication.
fn generic_param_ident(p: &syn::GenericParam) -> String {
    match p {
        syn::GenericParam::Type(t) => t.ident.to_string(),
        syn::GenericParam::Lifetime(l) => l.lifetime.ident.to_string(),
        syn::GenericParam::Const(c) => c.ident.to_string(),
    }
}

/// Merge the generic parameters of two `TypeEntry`s into a single `syn::Generics`,
/// deduplicating by parameter identifier (prevents `<T, T>` when both sources share
/// the same type variable).
///
/// Where-clause predicates from both sides are combined without deduplication
/// (identical predicates are harmless; de-duplicating them would require deeper
/// semantic comparison).
fn merge_generics(entry_a: &TypeEntry, entry_b: &TypeEntry) -> syn::Generics {
    let g_a = parse_stored_generics(entry_a);
    let g_b = parse_stored_generics(entry_b);

    let existing: std::collections::HashSet<String> =
        g_a.params.iter().map(generic_param_ident).collect();

    let mut merged = g_a;
    for param in g_b.params {
        if !existing.contains(&generic_param_ident(&param)) {
            merged.params.push(param);
        }
    }
    if let Some(wc_b) = g_b.where_clause {
        merged.make_where_clause().predicates.extend(wc_b.predicates);
    }
    merged
}

