/// Re-parse stored generics/where strings back into `syn::Generics`.
fn parse_stored_generics(entry: &TypeEntry) -> syn::Generics {
    if !entry.is_generic() {
        return syn::Generics::default();
    }
    let src = format!(
        "struct __D{} {} {{}}",
        entry.generics_tokens, entry.where_tokens
    );
    syn::parse_str::<DeriveInput>(&src)
        .map(|di| di.generics)
        .unwrap_or_default()
}

/// Determine the effective generics to use for a generated type.
///
/// Priority:
///   1. User-supplied `explicit` generics (non-empty) — honour exactly.
///   2. Source entry's registered generics (auto-inherit, only for anonymous intermediates).
///   3. Empty generics (non-generic type).
fn effective_generics(
    explicit: &syn::Generics,
    source_entry: &TypeEntry,
    allow_inherit: bool,
) -> syn::Generics {
    if !explicit.params.is_empty() || explicit.where_clause.is_some() {
        explicit.clone()
    } else if allow_inherit && source_entry.is_generic() {
        parse_stored_generics(source_entry)
    } else {
        syn::Generics::default()
    }
}

/// Returns a compile error when a source type is generic but the target declaration
/// provides no explicit generic parameters.
///
/// Only applied when `allow_inherit = false` (top-level `typex!` invocations).
/// Anonymous intermediates generated by composed expressions are exempt.
fn check_no_implicit_generics(
    source_ident: &Ident,
    source_entry: &TypeEntry,
    target_generics: &syn::Generics,
) -> Option<TokenStream> {
    if source_entry.is_generic()
        && target_generics.params.is_empty()
        && target_generics.where_clause.is_none()
    {
        Some(
            syn::Error::new(
                source_ident.span(),
                format!(
                    "type `{src}` has generic parameters; \
                     declare them explicitly in `typex!`, \
                     e.g. `Target<T> = {src}<T>`",
                    src = source_ident,
                ),
            )
            .to_compile_error(),
        )
    } else {
        None
    }
}

/// Split generics into the three parts needed for code generation:
/// `(impl_generics, ty_generics, where_clause)` as `TokenStream`s.
fn split(generics: &syn::Generics) -> (TokenStream, TokenStream, TokenStream) {
    let (impl_g, ty_g, where_c) = generics.split_for_impl();
    (quote!(#impl_g), quote!(#ty_g), quote!(#where_c))
}

/// Encode a `syn::Generics` back to `(generics_tokens, where_tokens)` strings
/// for storage in `TypeEntry`.
fn encode_generics(g: &syn::Generics) -> (String, String) {
    let params = &g.params;
    let generics_str = if params.is_empty() {
        String::new()
    } else {
        quote!(<#params>).to_string()
    };
    let where_str = g
        .where_clause
        .as_ref()
        .map(|wc| quote!(#wc).to_string())
        .unwrap_or_default();
    (generics_str, where_str)
}

/// Build the source type as it appears in an `impl` block:
/// `SourceIdent<ty_args>`.
///
/// - If the user supplied explicit ty_args on the source node, use them.
/// - Otherwise, use the target's `ty_generics` (appropriate for unary ops
///   where source and target share the same type params).
fn source_type(
    ident: &Ident,
    explicit_ty_args: Option<&TokenStream>,
    fallback_ty_g: &TokenStream,
) -> TokenStream {
    match explicit_ty_args {
        Some(args) => quote!(#ident #args),
        None => quote!(#ident #fallback_ty_g),
    }
}

