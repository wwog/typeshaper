pub fn expand_shape(input: ShapeInput) -> TokenStream {
    let attrs = input.attrs;
    let target = input.target;
    let target_generics = input.target_generics;
    let hint = target.to_string();
    let mut acc: Vec<TokenStream> = Vec::new();

    let result = expand_expr(input.expr, &attrs, target, &target_generics, false, &hint, &mut acc);

    match result {
        Ok(main_ts) => {
            let mut output = TokenStream::new();
            for ts in acc {
                output.extend(ts);
            }
            output.extend(main_ts);
            output
        }
        Err(ts) => ts,
    }
}

pub fn register_typeshaper(input: DeriveInput) -> TokenStream {
    let file = crate_key();
    let type_name = input.ident.to_string();
    let entry = match extract_type_entry(&input) {
        Ok(e) => e,
        Err(e) => return e,
    };
    register(file, type_name, entry);
    quote! { #input }
}

pub fn register_typeshaper_export(input: DeriveInput) -> TokenStream {
    match try_register_typeshaper_export(input) {
        Ok(ts) => ts,
        Err(ts) => ts,
    }
}

fn try_register_typeshaper_export(input: DeriveInput) -> R {
    let type_ident = &input.ident;
    let file = crate_key();
    let type_name = type_ident.to_string();

    let entry = extract_type_entry(&input)?;

    register(file, type_name.clone(), entry.clone());
    register_exported(type_name, entry.clone());

    let macro_name = format!("typeshaper_import_{}", type_ident);
    let macro_ident: Ident = syn::parse_str(&macro_name).map_err(|e| {
        syn::Error::new(
            type_ident.span(),
            format!("typeshaper: cannot form macro name `{}`: {}", macro_name, e),
        )
        .to_compile_error()
    })?;

    // Encode generics and where clause as bracket-wrapped token groups.
    // Format: `__typeshaper_import!(TypeName, [<generics>], [where ...], fields...)`
    let generics_ts: TokenStream = entry.generics_tokens.parse().map_err(|e| {
        syn::Error::new(
            proc_macro2::Span::call_site(),
            format!(
                "typeshaper: failed to re-parse generics tokens `{}`: {}",
                entry.generics_tokens, e
            ),
        )
        .to_compile_error()
    })?;
    let where_ts: TokenStream = entry.where_tokens.parse().map_err(|e| {
        syn::Error::new(
            proc_macro2::Span::call_site(),
            format!(
                "typeshaper: failed to re-parse where tokens `{}`: {}",
                entry.where_tokens, e
            ),
        )
        .to_compile_error()
    })?;

    let field_entries: Vec<TokenStream> = entry
        .fields
        .iter()
        .map(|f| -> R {
            let fname: Ident = syn::parse_str(&f.name).map_err(|e| {
                syn::Error::new(
                    proc_macro2::Span::call_site(),
                    format!("typeshaper: invalid field name `{}`: {}", f.name, e),
                )
                .to_compile_error()
            })?;
            let ftype: TokenStream = f.ty_tokens.parse().map_err(|e| {
                syn::Error::new(
                    proc_macro2::Span::call_site(),
                    format!("typeshaper: invalid type tokens `{}`: {}", f.ty_tokens, e),
                )
                .to_compile_error()
            })?;
            let fvis: TokenStream = f.vis.parse().map_err(|e| {
                syn::Error::new(
                    proc_macro2::Span::call_site(),
                    format!("typeshaper: invalid visibility `{}`: {}", f.vis, e),
                )
                .to_compile_error()
            })?;
            if let Some(ref inner) = f.unwrapped_ty {
                let inner_ts: TokenStream = inner.parse().map_err(|e| {
                    syn::Error::new(
                        proc_macro2::Span::call_site(),
                        format!("typeshaper: invalid inner type `{}`: {}", inner, e),
                    )
                    .to_compile_error()
                })?;
                Ok(quote! { #fvis #fname : #ftype [#inner_ts] })
            } else {
                Ok(quote! { #fvis #fname : #ftype })
            }
        })
        .collect::<Result<Vec<_>, _>>()?;

    Ok(quote! {
        #input

        #[macro_export]
        macro_rules! #macro_ident {
            () => {
                ::typeshaper::__typeshaper_import!(
                    #type_ident,
                    [#generics_ts],
                    [#where_ts],
                    #(#field_entries),*
                );
            };
        }
    })
}

/// Consume the output of `__typeshaper_import!`: parse inline field tokens
/// (plus generics/where metadata) and register the type in the import namespace.
pub fn expand_import(input: ImportInput) -> TokenStream {
    let type_name = input.type_name.to_string();

    // Decode generics/where from the bracket groups.
    let generics_tokens = input.generics_tokens.to_string();
    let where_tokens = input.where_tokens.to_string();

    let fields: Vec<FieldDef> = input
        .fields
        .iter()
        .map(|(name, ty, unwrapped, vis)| {
            let vis_str = vis.to_string();
            if let Some(inner) = unwrapped {
                FieldDef::wrapped_optional(name.to_string(), vis_str, inner.to_string())
            } else {
                FieldDef::plain(name.to_string(), vis_str, ty.to_string())
            }
        })
        .collect();

    let entry = TypeEntry::with_generics(fields, generics_tokens, where_tokens);
    register(None, type_name, entry);
    TokenStream::new()
}

